package lecture4;

public class while_loop {
    public static void main(String[] args) {
        int i=0;
        //it is entry control loop.
        //it first check the condition the execute the statement inside the loop
        //if 1st iteration the condition is false then it directly exit without go inside the loop.
        while (i<=10) 
        {
            System.out.println(i);
            i++;
        }
    }
    
}
